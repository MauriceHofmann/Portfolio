//Variablen gibt es in JavaScript in Form von zwei Typen.
//Diese werden entweder mit dem Schlüsselwort var oder let erstellt. Der Uunterschied liegt hierbei im Gültigkeitsbereich.
//Vorerst ist der Variablentyp egal. Auf den Gültigkeitsbereich wird in einem späteren Zeitpunkt genauer eingegangen.

//Deklaration von Variablen
//Schlüsselwort und individueller Name;
var variableEins;
var variableeins;
let variableZwei;

//Initialisierung von Variablen
//Variablenname und Zweisung eines Wertes;
variableEins = 1;
variableeins = 111;
variableZwei = 2;

//Deklaration und Initialisierung vereinfacht
var variableDrei = 3;

//Im laufe des Programmes kann sich der Wert einer Variable ändern. 
//Im Gegensatz dazu gibt es Konstanten, die festgelegte Werte enthalten. Dies erfolgt mit dem Schlüsselwort const.
//Hierbei muss jedoch die Initialisierung mit der Deklaration erfolgen.
//Schlüsselwort und individueller Name
const konstanteVier = 4;

//Ausgabe auf der HTML-Seite
//Mit der Methode Alert des Windowsobjekts wird ein Ausgabefenster im Browser erzwungen.
//In den Methodenklammern folgt die Ausgabe
alert(variableEins);

